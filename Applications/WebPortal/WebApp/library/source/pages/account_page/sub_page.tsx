/** Lists the sub-pages displayed in the body of the account page. */
export enum SubPage {

  /** No sub-page. */
  NONE,

  /** The profile page. */
  PROFILE,

  /** The risk controls page. */
  RISK_CONTROLS,

  /** The entitlements page. */
  ENTITLEMENTS,

  /** The compliance page. */
  COMPLIANCE,

  /** The profit and loss page. */
  PROFIT_LOSS
}
